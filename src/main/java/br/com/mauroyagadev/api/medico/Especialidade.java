package br.com.mauroyagadev.api.medico;

/*Esta classe enum define as especialidades médicas disponíveis no sistema.
* Cada especialidade é representada por uma constante.
* As constantes são ORTOPEDIA, CARDIOLOGIA, GINECOLOGIA e DERMATOLOGIA.
* A classe enum é utilizada para garantir que apenas as especialidades definidas sejam utilizadas no sistema.
* A classe enum é utilizada para representar um conjunto fixo de valores que não podem ser alterados.
* As classe enum são utilizadas para representar tipos de dados que possuem
* um número limitado de valores possíveis.
*
* */

public enum Especialidade {
    ORTOPEDIA,
    CARDIOLOGIA,
    GINECOLOGIA,
    DERMATOLOGIA;
}
